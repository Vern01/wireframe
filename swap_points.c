#include "fdf.h"

void	swap_points(t_cor *cor)
{
	int		temp;

	temp = cor->x1;
	cor->x1 = cor->x2;
	cor->x2 = temp;
	temp = cor->y1;
	cor->y1 = cor->y2;
	cor->y2 = temp;
}
